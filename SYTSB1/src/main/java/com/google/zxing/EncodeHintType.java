public enum EncodeHintType {

	/**
	   * Specifies what degree of error correction to use, for example in QR Codes (type Integer).
	   */
	  ERROR_CORRECTION,

	  /**
	   * Specifies what character encoding to use where applicable (type String)
	   */
	  CHARACTER_SET,
	  
	  MARGIN
}
