package com.lsts.webservice.cxf.server.report;

/**
 * 设备信息数据传输对象
 * @ClassName DeviceDTO
 * @JDK 1.7
 * @author GaoYa
 * @date 2014-09-16 上午09:35:00
 */
public class DeviceDTO {
	private String device_type;		// 设备类型
	private String com_name;		// 使用单位
	private String internal_num; 	// 单位内部编号（自编号）
	private String device_registration_code;	// 设备注册代码
	private String check_type;		// 检验类别
	private String check_date;		// 检验日期
	private String check_result;	// 检验结果
	private String check_status;	// 检验状态（）
	private Double advance_fees	;//	预收金额

	public DeviceDTO(String device_type, String com_name, String internal_num,
			String device_registration_code, String check_type,
			String check_date, String check_result, String check_status, Double advance_fees) {
		super();
		this.device_type = device_type;
		this.com_name = com_name;
		this.internal_num = internal_num;
		this.device_registration_code = device_registration_code;
		this.check_type = check_type;
		this.check_date = check_date;
		this.check_result = check_result;
		this.check_status = check_status;
		this.advance_fees = advance_fees;
	}

	public String getDevice_type() {
		return device_type;
	}

	public void setDevice_type(String device_type) {
		this.device_type = device_type;
	}

	public String getCom_name() {
		return com_name;
	}

	public void setCom_name(String com_name) {
		this.com_name = com_name;
	}

	public String getInternal_num() {
		return internal_num;
	}

	public void setInternal_num(String internal_num) {
		this.internal_num = internal_num;
	}

	public String getDevice_registration_code() {
		return device_registration_code;
	}

	public void setDevice_registration_code(String device_registration_code) {
		this.device_registration_code = device_registration_code;
	}

	public String getCheck_type() {
		return check_type;
	}

	public void setCheck_type(String check_type) {
		this.check_type = check_type;
	}

	public String getCheck_date() {
		return check_date;
	}

	public void setCheck_date(String check_date) {
		this.check_date = check_date;
	}

	public String getCheck_result() {
		return check_result;
	}

	public void setCheck_result(String check_result) {
		this.check_result = check_result;
	}

	public String getCheck_status() {
		return check_status;
	}

	public void setCheck_status(String check_status) {
		this.check_status = check_status;
	}

	public Double getAdvance_fees() {
		return advance_fees;
	}

	public void setAdvance_fees(Double advance_fees) {
		this.advance_fees = advance_fees;
	}
	
	
}
