package com.lsts.employee.bean;

/**
 * 人员证书预警数据传输对象 
 * EmployeeDTO
 * 
 * @author GaoYa
 * @date 2014-04-17 下午04:58:00
 */
public class EmployeeDTO {

	// Fields
	private String name;
	private String org_name;
	private String employee_id;
	private float warn_days;

	// Constructors

	/** default constructor */
	public EmployeeDTO() {
	}

	/** full constructor */
	public EmployeeDTO(String name, String org_name, String employee_id, float warn_days) {
		this.name = name;
		this.org_name = org_name;
		this.employee_id = employee_id;
		this.warn_days = warn_days;
	}

	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getOrg_name() {
		return org_name;
	}

	public void setOrg_name(String org_name) {
		this.org_name = org_name;
	}

	public String getEmployee_id() {
		return employee_id;
	}

	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}

	public float getWarn_days() {
		return warn_days;
	}

	public void setWarn_days(float warn_days) {
		this.warn_days = warn_days;
	}

}