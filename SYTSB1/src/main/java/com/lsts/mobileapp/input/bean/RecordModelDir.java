package com.lsts.mobileapp.input.bean;

public class RecordModelDir {
	
	private String fkModelId; //原始记录模板code
    private String pageCode; //模板名称
    private String pageName; //版本号
    private String pagePath; //模板路劲
    private Integer orders; //顺序
    
    
	public String getFkModelId() {
		return fkModelId;
	}
	public void setFkModelId(String fkModelId) {
		this.fkModelId = fkModelId;
	}
	public String getPageCode() {
		return pageCode;
	}
	public void setPageCode(String pageCode) {
		this.pageCode = pageCode;
	}
	public String getPageName() {
		return pageName;
	}
	public void setPageName(String pageName) {
		this.pageName = pageName;
	}
	public String getPagePath() {
		return pagePath;
	}
	public void setPagePath(String pagePath) {
		this.pagePath = pagePath;
	}
	public Integer getOrders() {
		return orders;
	}
	public void setOrders(Integer orders) {
		this.orders = orders;
	}
    
    
	
}
