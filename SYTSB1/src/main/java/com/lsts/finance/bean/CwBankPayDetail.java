package com.lsts.finance.bean;


/**
 *银行转账的收费明细bean
 *
 * @author dxf
 *
 * @date 2015年11月20日
 */
public class CwBankPayDetail {


	private String id;//id
	
    private java.util.Date payTime;//收费时间

    private java.math.BigDecimal payMoney;//转入金额

    private String receiveName;//收费人姓名
    
    private String payName;//缴费人姓名
    
    private String invoiceNum;//发票号
    
    private String doType;//操作类型
    
    private String doContent;//操作内容

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public java.util.Date getPayTime() {
		return payTime;
	}

	public void setPayTime(java.util.Date payTime) {
		this.payTime = payTime;
	}

	public java.math.BigDecimal getPayMoney() {
		return payMoney;
	}

	public void setPayMoney(java.math.BigDecimal payMoney) {
		this.payMoney = payMoney;
	}

	public String getPayName() {
		return payName;
	}

	public void setPayName(String payName) {
		this.payName = payName;
	}

	public String getInvoiceNum() {
		return invoiceNum;
	}

	public void setInvoiceNum(String invoiceNum) {
		this.invoiceNum = invoiceNum;
	}

	public String getReceiveName() {
		return receiveName;
	}

	public void setReceiveName(String receiveName) {
		this.receiveName = receiveName;
	}

	public String getDoType() {
		return doType;
	}

	public void setDoType(String doType) {
		this.doType = doType;
	}

	public String getDoContent() {
		return doContent;
	}

	public void setDoContent(String doContent) {
		this.doContent = doContent;
	}
	
	
} 
