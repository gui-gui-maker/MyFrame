package com.lsts.finance.bean;


/**
 *银行转账的退款明细bean
 *
 * @author dxf
 *
 * @date 2015年11月20日
 */
public class CwBankBackDetail {


	private String id;//id
	
    private java.util.Date operatorTime;//操作时间

    private java.math.BigDecimal fefundMoney;//退款金额

    private String operatorName;//操作人
    
    private String unitName;//退款单位
    
    private String fefundReason;//退款原因

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public java.util.Date getOperatorTime() {
		return operatorTime;
	}

	public void setOperatorTime(java.util.Date operatorTime) {
		this.operatorTime = operatorTime;
	}

	public java.math.BigDecimal getFefundMoney() {
		return fefundMoney;
	}

	public void setFefundMoney(java.math.BigDecimal fefundMoney) {
		this.fefundMoney = fefundMoney;
	}

	public String getOperatorName() {
		return operatorName;
	}

	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}

	public String getFefundReason() {
		return fefundReason;
	}

	public void setFefundReason(String fefundReason) {
		this.fefundReason = fefundReason;
	}
	
} 
