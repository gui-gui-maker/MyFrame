package com.lsts.inspection.bean;

import java.util.Date;

/**
 * 待处理业务、待处理公文数据传输对象 
 * FlowInfoDTO
 * @author GaoYa
 * @date 2014-04-17 上午09:58:00
 */
public class FlowInfoDTO {

	// Fields
	private String id;
	private String activity_name;
	private String flow_name;
	private String num;
	private String activity_id;
	private String function;
	private String fk_flow_index_id;
	private String title;
	private Date creater_time;

	// Constructors

	/** default constructor */
	public FlowInfoDTO() {
	}

	/** full constructor */
	public FlowInfoDTO(String id, String activity_name, String flow_name,
			String num, String activity_id, String function,
			String fk_flow_index_id, String title, Date creater_time) {
		this.id = id;
		this.activity_name = activity_name;
		this.flow_name = flow_name;
		this.num = num;
		this.activity_id = activity_id;
		this.function = function;
		this.fk_flow_index_id = fk_flow_index_id;
		this.title = title;
		this.creater_time = creater_time;
	}

	public String getActivity_name() {
		return activity_name;
	}

	public void setActivity_name(String activity_name) {
		this.activity_name = activity_name;
	}

	public String getFlow_name() {
		return flow_name;
	}

	public void setFlow_name(String flow_name) {
		this.flow_name = flow_name;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getActivity_id() {
		return activity_id;
	}

	public void setActivity_id(String activity_id) {
		this.activity_id = activity_id;
	}

	public String getFunction() {
		return function;
	}

	public void setFunction(String function) {
		this.function = function;
	}

	public String getFk_flow_index_id() {
		return fk_flow_index_id;
	}

	public void setFk_flow_index_id(String fk_flow_index_id) {
		this.fk_flow_index_id = fk_flow_index_id;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Date getCreater_time() {
		return creater_time;
	}

	public void setCreater_time(Date creater_time) {
		this.creater_time = creater_time;
	}

}