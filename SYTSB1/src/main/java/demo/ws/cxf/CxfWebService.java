package demo.ws.cxf;

import javax.jws.WebService;

/**
 * 演示样例——CXF web service。
 * 
 * 这里定义了一个interface，是 cxf web servcie的接口规范
 */
@WebService
// 标注为一个web service
public interface CxfWebService {

	/**
	 * 定义一个接口方法
	 * 
	 * @return
	 * @throws Exception
	 */
	public String serviceMethod() throws Exception;
}
